/*
Increment and Decrement Operators

Increment Operator: This operator is used to increase the value by one. 
The prefix notation ++num increases the value of the variable first, then returns a new value. 
The postfix notation num++ returns the current value of the variable first, then increases it.
*/

let x = 5;

console.log(++x); // 6
console.log(x); // 6


let y = 5;

console.log(y++); // 5
console.log(y); // 6

/*
Decrement Operator: This operator is used to decrease the value by one. 
The prefix and postfix notation works the same way as earlier with the increment operator.
*/

let num = 5;

console.log(--num); // 4
console.log(num--); // 4
console.log(num); // 3
